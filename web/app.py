def load_config(name: str):
    meta = CONFIGS[name]
    path = CONFIG_PATH / meta["file"]
    if not path.exists():
        return {} if meta["type"] in ("dict", "raw") else []
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


_bot_config = None  # Set by main.py when running combined — allows live reload.


def set_bot_config(config) -> None:
    global _bot_config
    _bot_config = config


def save_config(name: str, data) -> None:
    meta = CONFIGS[name]
    path = CONFIG_PATH / meta["file"]
    tmp = path.with_suffix(".tmp")
    with open(tmp, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2, ensure_ascii=False)
    tmp.replace(path)
    # Reload the bot's in-memory config so changes take effect immediately.
    if _bot_config is not None:
        _bot_config.reload_all()


