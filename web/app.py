def _config_ctx(name: str, extra: dict) -> dict:
    return {"active_page": "configs", "db_path": DB_PATH,
            "config_name": name, "config_meta": CONFIGS[name], **extra}


@app.get("/configs", response_class=HTMLResponse, dependencies=[Depends(require_auth)])
async def configs_overview(request: Request):
    items = []
    for name, meta in CONFIGS.items():
        data = load_config(name)
        items.append({
            "name": name, "label": meta["label"], "description": meta["description"],
            "type": meta["type"], "count": len(data),
            "auto_generated": meta.get("auto_generated", False),
        })
    return templates.TemplateResponse(
        request, "configs.html",
        {"active_page": "configs", "db_path": DB_PATH, "items": items},
    )


@app.get("/configs/{name}", response_class=HTMLResponse, dependencies=[Depends(require_auth)])
async def config_view(
    request: Request,
    name: str,
    search: str = Query(""),
    page: int = Query(1, ge=1),
):
    if name not in CONFIGS:
        raise HTTPException(status_code=404, detail="Config not found")

    meta = CONFIGS[name]
    data = load_config(name)
    saved = request.query_params.get("saved") == "1"

    if meta["type"] == "dict":
        entries = list(data.items())
        if search:
            sl = search.lower()
            entries = [(k, v) for k, v in entries if sl in k.lower() or sl in str(v).lower()]
        total = len(entries)
        total_pages = max(1, (total + CONFIG_PAGE_SIZE - 1) // CONFIG_PAGE_SIZE)
        page = min(page, total_pages)
        page_entries = entries[(page - 1) * CONFIG_PAGE_SIZE: page * CONFIG_PAGE_SIZE]
        ctx = _config_ctx(name, {
            "entries": page_entries, "total": total, "total_count": len(data),
            "page": page, "total_pages": total_pages, "search": search, "saved": saved,
        })
    elif meta["type"] == "list":
        items = data
        if search:
            sl = search.lower()
            items = [i for i in items if sl in i.lower()]
        ctx = _config_ctx(name, {
            "items": items, "total": len(data), "search": search, "saved": saved,
        })
    else:
        ctx = _config_ctx(name, {
            "raw_content": json.dumps(data, indent=2, ensure_ascii=False),
            "saved": saved, "error": None,
        })

    return templates.TemplateResponse(request, "config.html", ctx)


@app.post("/configs/{name}/add", dependencies=[Depends(require_auth)])
async def config_add(name: str, key: str = Form(""), value: str = Form(""), item: str = Form("")):
    if name not in CONFIGS:
        raise HTTPException(status_code=404)
    meta = CONFIGS[name]
    data = load_config(name)
    if meta["type"] == "dict" and key.strip():
        data[key.strip()] = value.strip()
        save_config(name, data)
    elif meta["type"] == "list" and item.strip() and item.strip() not in data:
        data.append(item.strip())
        save_config(name, data)
    return RedirectResponse(f"/configs/{name}?saved=1", status_code=303)


@app.post("/configs/{name}/edit", dependencies=[Depends(require_auth)])
async def config_edit_entry(
    request: Request,
    name: str,
    old_key: str = Form(),
    new_key: str = Form(),
    new_value: str = Form(),
):
    if name not in CONFIGS or CONFIGS[name]["type"] != "dict":
        raise HTTPException(status_code=404)
    data = load_config(name)
    if old_key in data:
        del data[old_key]
    if new_key.strip():
        data[new_key.strip()] = new_value.strip()
    save_config(name, data)
    referer = request.headers.get("referer", f"/configs/{name}")
    return RedirectResponse(referer, status_code=303)


@app.post("/configs/{name}/delete", dependencies=[Depends(require_auth)])
async def config_delete_entry(
    request: Request,
    name: str,
    key: str = Form(""),
    item: str = Form(""),
):
    if name not in CONFIGS:
        raise HTTPException(status_code=404)
    meta = CONFIGS[name]
    data = load_config(name)
    if meta["type"] == "dict" and key in data:
        del data[key]
        save_config(name, data)
    elif meta["type"] == "list" and item in data:
        data.remove(item)
        save_config(name, data)
    referer = request.headers.get("referer", f"/configs/{name}")
    return RedirectResponse(referer, status_code=303)


@app.post("/configs/{name}/save-raw", dependencies=[Depends(require_auth)])
async def config_save_raw(request: Request, name: str, content: str = Form()):
    if name not in CONFIGS or CONFIGS[name]["type"] != "raw":
        raise HTTPException(status_code=404)
    try:
        data = json.loads(content)
    except json.JSONDecodeError as exc:
        return templates.TemplateResponse(
            request, "config.html",
            _config_ctx(name, {"raw_content": content, "error": f"Invalid JSON: {exc}", "saved": False}),
            status_code=400,
        )
    save_config(name, data)
    return RedirectResponse(f"/configs/{name}?saved=1", status_code=303)
